using System;

namespace Foundation.Resources
{
    /// <summary>
    /// Defines the Font Size for all parts of the application
    /// </summary>
    public class FontSize
    {
        /// <summary>
        /// Gets the size of the header font.
        /// </summary>
        /// <value>
        /// The size of the header font.
        /// </value>
        public static Double HeaderFontSize => 20;

        /// <summary>
        /// Gets the size of the screen name font.
        /// </summary>
        /// <value>
        /// The size of the screen name font.
        /// </value>
        public static Double ScreenNameFontSize => 18;

        /// <summary>
        /// Gets the size of the screen instructions font.
        /// </summary>
        /// <value>
        /// The size of the screen instructions font.
        /// </value>
        public static Double ScreenInstructionsFontSize => 14;

        /// <summary>
        /// Gets the default size of the font.
        /// </summary>
        /// <value>
        /// The default size of the font.
        /// </value>
        public static Double DefaultFontSize => 15;

        /// <summary>
        /// Gets the size of the small header font.
        /// </summary>
        /// <value>
        /// The size of the small header font.
        /// </value>
        public static Double SmallHeaderFontSize => 12;

        /// <summary>
        /// Gets the size of the small content font.
        /// </summary>
        /// <value>
        /// The size of the small content font.
        /// </value>
        public static Double SmallContentFontSize => 10;
    }
}
